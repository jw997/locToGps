function fixIntersections(thisCounty) {
	const borderCounty = mapCountyToBoundary.get(thisCounty);
	const borderCities = new Map();

	for (const cityName of mapCountyToCities.get(thisCounty)) {
		borderCities.set(cityName, mapCityToBoundary.get(cityName))
	}
	const intersectionJson = getJson('./temp/input/intersections_' + fileNameIze(thisCounty) + '.json');

	for (const f of intersectionJson.features) {
		addCityCountyToIntersectionGeoJson(f, thisCounty, borderCounty, borderCities);
	}

	writeJson('./temp/output/intersections_' + fileNameIze(thisCounty) + '.json', intersectionJson);
}

