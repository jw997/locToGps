function addCityCountyToIntersectionGeoJson(f, countyName, countyBorder, mapCityBorders) {

	const ptInter = turf.point(f.geometry.coordinates);

	const bInCounty = turf.inside(ptInter, countyBorder);
	if (bInCounty) {
		f.properties.County = countyName;
	} else {
		const dist = pointToPolygonDistance(ptInter, countyBorder, { units: 'meters' })  // kilometers, miles, meters
		console.log('County', countyName, 'does not contain', f.properties.streets, dist)

		return
	}

	for (const [k, v] of mapCityBorders.entries()) {
		const bInside = turf.inside(ptInter, v);
		if (bInside) {
			f.properties.City = k;
			break;
		}
	}
	if (!f.properties.City) {
		f.properties.City = 'Unincorporated'
	}
}

